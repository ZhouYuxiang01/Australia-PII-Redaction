def build_hard_negative_teacher_prompts(
    training_labels: set[str],
    *,
    examples_per_scenario: int = 2,
    self_consistency: int = 1,
) -> tuple[list[dict[str, Any]], dict[str, Any]]:
    if "NON_PII" not in training_labels:
        raise ValueError("training label space must include NON_PII")
    if examples_per_scenario <= 0:
        raise ValueError("examples_per_scenario must be positive")
    if self_consistency <= 0:
        raise ValueError("self_consistency must be positive")

    prompts: list[dict[str, Any]] = []
    skipped: list[dict[str, Any]] = []
    base_index = 0
    for scenario in HARD_NEGATIVE_SCENARIOS:
        candidates = compatible_candidates(scenario["candidate_labels"], training_labels)
        if len(candidates) <= 1:
            skipped.append({"id": scenario["id"], "reason": "not enough labels in training space", "candidate_labels": candidates})
            continue
        contexts = scenario["contexts"]
        for example_index in range(examples_per_scenario):
            base_index += 1
            context = contexts[example_index % len(contexts)]
            base_id = f"STAGE2-HARDNEG-BASE-{base_index:04d}"
            for consistency_index in range(1, self_consistency + 1):
                prompt = {
                    "id": f"{base_id}-SC{consistency_index}",
                    "base_prompt_id": base_id,
                    "self_consistency_index": consistency_index,
                    "ambiguity_group": f"hard_negative_{scenario['id']}",
                    "context_type": HARD_NEGATIVE_CONTEXT_TYPE,
                    "pilot_category": "candidate_level_hard_negative",
                    "generation_bucket": "candidate_level_hard_negative",
                    "span_value": scenario["span_value"],
                    "context": context,
                    "candidate_labels": candidates,
                    "expected_top_type": "NON_PII",
                    "hard_negative_reason": scenario["reason"],
                    "teacher_model_path": DEFAULT_TEACHER_MODEL_PATH,
                    "teacher_call_status": "not_run",
                }
                prompt["prompt"] = build_json_only_prompt(prompt)
                prompts.append(prompt)

    report = {
        "teacher_prompt_count": len(prompts),
        "base_prompt_count": len({prompt["base_prompt_id"] for prompt in prompts}),
        "examples_per_scenario": examples_per_scenario,
        "self_consistency": self_consistency,
        "context_type": HARD_NEGATIVE_CONTEXT_TYPE,
        "scenario_count": len(HARD_NEGATIVE_SCENARIOS),
        "skipped_scenarios": skipped,
        "candidate_label_counts": dict(sorted(Counter(label for prompt in prompts for label in prompt["candidate_labels"]).items())),
        "scenario_prompt_counts": dict(sorted(Counter(prompt["ambiguity_group"] for prompt in prompts).items())),
        "merged_into_training_dataset": False,
        "student_training_started": False,
    }
    return prompts, report


def compatible_candidates(candidate_labels: list[str], training_labels: set[str]) -> list[str]:
    labels = [label for label in candidate_labels if label in training_labels and label != "NON_PII"]
    labels.append("NON_PII")
    return dedupe(labels)


def dedupe(labels: list[str]) -> list[str]:
    seen: set[str] = set()
    output: list[str] = []
    for label in labels:
        if label in seen:
            continue
        seen.add(label)
        output.append(label)
    return output


