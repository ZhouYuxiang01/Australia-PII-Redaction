async def run_hard_negative_project_async(
    root: Path | str = ".",
    *,
    base_url: str = "http://localhost:8000/v1",
    model_name: str = DEFAULT_TEACHER_MODEL_PATH,
    concurrency: int = 16,
    max_tokens: int = 96,
    timeout_seconds: float = 240.0,
    max_retries: int = 1,
    examples_per_scenario: int = 2,
    self_consistency: int = 1,
    sender: AsyncSender | None = None,
) -> dict[str, Any]:
    root = Path(root)
    generated_dir = root / "data" / "generated"
    reports_dir = root / "reports"
    generated_dir.mkdir(parents=True, exist_ok=True)
    reports_dir.mkdir(parents=True, exist_ok=True)
    training_labels = set(json.loads((root / "pii_schema" / "training_label_space_80.json").read_text(encoding="utf-8")))

    prompts, prompt_plan = build_hard_negative_teacher_prompts(
        training_labels,
        examples_per_scenario=examples_per_scenario,
        self_consistency=self_consistency,
    )
    prompt_path = generated_dir / "stage2_hard_negative_teacher_prompts.jsonl"
    raw_path = generated_dir / "stage2_hard_negative_teacher_raw.jsonl"
    converted_path = generated_dir / "stage2_hard_negative_teacher_converted.jsonl"
    progress_path = reports_dir / "stage2_hard_negative_teacher_progress.json"
    generation_report_path = reports_dir / "stage2_hard_negative_teacher_generation_report.json"
    errors_path = reports_dir / "stage2_hard_negative_teacher_errors.json"
    quality_path = reports_dir / "stage2_hard_negative_teacher_quality_report.json"
    warnings_path = reports_dir / "stage2_hard_negative_teacher_warning_examples.json"

    write_jsonl(prompt_path, prompts)
    new_rows, runtime = await run_full_teacher_async(
        prompts,
        raw_path=raw_path,
        progress_path=progress_path,
        base_url=base_url,
        model_name=model_name,
        concurrency=concurrency,
        max_tokens=max_tokens,
        timeout_seconds=timeout_seconds,
        max_retries=max_retries,
        progress_interval=100,
        sender=sender,
    )
    raw_rows = load_all_raw_rows(raw_path)
    converted_rows, conversion_errors = convert_raw_outputs(raw_rows, training_labels)
    validation = validate_converted_rows(converted_rows, training_labels)
    write_jsonl(converted_path, converted_rows)

    generation_report = build_report(
        raw_rows,
        converted_rows,
        conversion_errors,
        validation,
        backend="vllm_openai",
        wall_time_seconds=runtime["wall_time_seconds"],
    )
    generation_report.update(prompt_plan)
    generation_report.update(runtime)
    generation_report["new_rows_this_run"] = len(new_rows)
    generation_report["expected_teacher_calls"] = len(prompts)
    generation_report["teacher_calls_completed_total"] = len(raw_rows)
    generation_report["hard_negative_teacher_calls_completed"] = len(raw_rows) == len(prompts)
    generation_report["raw_output_written_incrementally"] = True
    generation_report["merged_into_training_dataset"] = False
    generation_report["student_training_started"] = False
    write_json(generation_report_path, generation_report)

    quality, warnings = analyze_quality(converted_rows)
    quality["conversion_validation"] = validation
    quality["inputs"] = {
        "converted_path": str(converted_path),
        "raw_path": str(raw_path),
        "raw_record_count": len(raw_rows),
    }
    quality["merged_into_training_dataset"] = False
    quality["student_training_started"] = False
    write_json(quality_path, quality)
    write_json(warnings_path, {"warning_examples": warnings, "warning_counts": quality["warning_counts"]})

    error_report = build_error_report(conversion_errors, validation, converted_rows)
    error_report["raw_status_counts"] = dict(sorted(Counter(str(row.get("status")) for row in raw_rows).items()))
    error_report["quality_warning_counts"] = quality["warning_counts"]
    error_report["merged_into_training_dataset"] = False
    error_report["student_training_started"] = False
    write_json(errors_path, error_report)
    return generation_report


def run_hard_negative_project(root: Path | str = ".", **kwargs: Any) -> dict[str, Any]:
    return asyncio.run(run_hard_negative_project_async(root, **kwargs))


class TransformersLocalTeacherRunner:
    def __init__(self, model_path: str, max_new_tokens: int):
        self.model_path = model_path
        self.max_new_tokens = max_new_tokens
        import torch
        from transformers import AutoModelForCausalLM, AutoModelForImageTextToText, AutoTokenizer

        self.torch = torch
        self.tokenizer = AutoTokenizer.from_pretrained(model_path, trust_remote_code=True, local_files_only=True, use_fast=True)
        if self.tokenizer.pad_token_id is None:
            self.tokenizer.pad_token = self.tokenizer.eos_token
        dtype = torch.bfloat16 if torch.cuda.is_available() and torch.cuda.is_bf16_supported() else torch.float16
        load_kwargs = {
            "trust_remote_code": True,
            "local_files_only": True,
            "dtype": dtype,
            "device_map": {"": 0} if torch.cuda.is_available() else None,
            "low_cpu_mem_usage": True,
        }
        try:
            self.model = AutoModelForImageTextToText.from_pretrained(model_path, **load_kwargs)
        except Exception:
            self.model = AutoModelForCausalLM.from_pretrained(model_path, **load_kwargs)
        self.model.eval()

    def generate(self, prompt: str) -> str:
        messages = [
            {"role": "system", "content": "You are a strict JSON generator for PII ambiguity verdicts. Return JSON only."},
            {"role": "user", "content": prompt},
        ]
        prompt_text = apply_chat_template_no_thinking(self.tokenizer, messages)
        inputs = self.tokenizer(prompt_text, return_tensors="pt")
        device = next(self.model.parameters()).device
        inputs = {key: value.to(device) for key, value in inputs.items()}
        with self.torch.no_grad():
            output_ids = self.model.generate(
                **inputs,
                max_new_tokens=self.max_new_tokens,
                do_sample=False,
                pad_token_id=self.tokenizer.pad_token_id,
                eos_token_id=self.tokenizer.eos_token_id,
            )
        new_tokens = output_ids[0, inputs["input_ids"].shape[-1] :]
        return self.tokenizer.decode(new_tokens, skip_special_tokens=True).strip()


def apply_chat_template_no_thinking(tokenizer: Any, messages: list[dict[str, str]]) -> str:
    try:
        return tokenizer.apply_chat_template(messages, tokenize=False, add_generation_prompt=True, enable_thinking=False)
    except TypeError:
        return tokenizer.apply_chat_template(messages, tokenize=False, add_generation_prompt=True)


def run_local_transformers_outputs(
    prompts: list[dict[str, Any]],
    *,
    raw_path: Path,
    model_path: str,
    max_new_tokens: int,
    runner_factory: RunnerFactory | None = None,
) -> list[dict[str, Any]]:
    raw_path.parent.mkdir(parents=True, exist_ok=True)
    completed = completed_raw_ids(raw_path)
    pending = [prompt for prompt in prompts if prompt["id"] not in completed]
    runner = (runner_factory or TransformersLocalTeacherRunner)(model_path, max_new_tokens)
    rows: list[dict[str, Any]] = []
    with raw_path.open("a", encoding="utf-8") as handle:
        for prompt in pending:
            started = time.time()
            output_text = ""
            status = "error"
            error = None
            try:
                output_text = runner.generate(build_json_only_prompt(prompt))
                extract_json_object(output_text)
                status = "ok"
            except Exception as exc:
                error = repr(exc)
                status = "malformed_or_error"
            row = {
                "id": prompt["id"],
                "prompt": prompt,
                "output_text": output_text if status == "ok" else "",
                "status": status,
                "error": error,
                "elapsed_seconds": round(time.time() - started, 3),
                "teacher_model_path": model_path,
                "generation_settings": {
                    "backend": "local_transformers",
                    "model": model_path,
                    "max_new_tokens": max_new_tokens,
                    "do_sample": False,
                    "flash_linear_attention_available": flash_linear_attention_available(),
                },
                "backend": "local_transformers",
                "retry_count": 0,
            }
            rows.append(row)
            handle.write(json.dumps(row, ensure_ascii=False, separators=(",", ":")) + "\n")
            handle.flush()
    return rows


def completed_raw_ids(raw_path: Path) -> set[str]:
    if not raw_path.exists():
        return set()
    completed: set[str] = set()
    for line in raw_path.read_text(encoding="utf-8").splitlines():
        if not line.strip():
            continue
        try:
            row = json.loads(line)
        except json.JSONDecodeError:
            continue
        if row.get("status") == "ok":
            completed.add(str(row.get("id")))
    return completed


def flash_linear_attention_available() -> bool:
    try:
        import fla  # noqa: F401

        return True
    except Exception:
        return False


