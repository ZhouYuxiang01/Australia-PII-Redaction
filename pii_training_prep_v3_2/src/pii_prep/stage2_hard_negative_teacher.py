    parser.add_argument("--root", default=".")
    parser.add_argument("--examples-per-scenario", type=int, default=2)
    parser.add_argument("--self-consistency", type=int, default=1)
    parser.add_argument("--run-teacher", action="store_true")
    parser.add_argument("--backend", choices=["vllm", "local"], default="vllm")
    parser.add_argument("--base-url", default="http://localhost:8000/v1")
    parser.add_argument("--model", default=DEFAULT_TEACHER_MODEL_PATH)
    parser.add_argument("--concurrency", type=int, default=16)
    parser.add_argument("--max-tokens", type=int, default=96)
    parser.add_argument("--timeout-seconds", type=float, default=240.0)
    parser.add_argument("--max-retries", type=int, default=1)
    args = parser.parse_args(argv)
    if args.run_teacher:
        if args.backend == "local":
            report = run_hard_negative_local_project(
                args.root,
                model_name=args.model,
                max_tokens=args.max_tokens,
                examples_per_scenario=args.examples_per_scenario,
                self_consistency=args.self_consistency,
            )
        else:
            report = run_hard_negative_project(
                args.root,
                base_url=args.base_url,
                model_name=args.model,
                concurrency=args.concurrency,
                max_tokens=args.max_tokens,
                timeout_seconds=args.timeout_seconds,
                max_retries=args.max_retries,
                examples_per_scenario=args.examples_per_scenario,
                self_consistency=args.self_consistency,
            )
    else:
        report = generate_hard_negative_teacher_artifacts(
            args.root,
            examples_per_scenario=args.examples_per_scenario,
            self_consistency=args.self_consistency,
        )
    print(json.dumps(report, ensure_ascii=False, indent=2))
    return 0


