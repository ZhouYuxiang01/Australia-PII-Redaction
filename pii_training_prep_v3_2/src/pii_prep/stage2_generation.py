def generate_teacher_prompt_samples(training_labels: set[str], prompt_sample_size: int) -> list[dict[str, Any]]:
    prompts: list[dict[str, Any]] = []
    for group in AMBIGUITY_GROUPS:
        candidates = filter_candidates(group["candidates"], training_labels)
        if len(candidates) <= 1:
            continue
        for context_type in CONTEXT_TYPES:
            context = group["contexts"][context_type]
            prompt_id = f"STAGE2-PROMPT-{len(prompts) + 1:03d}"
            prompts.append(
                {
                    "id": prompt_id,
                    "ambiguity_group": group["name"],
                    "context_type": context_type,
                    "span_value": group["span"],
                    "context": context,
                    "candidate_labels": candidates,
                    "prompt": build_5way_prompt(group["span"], context, candidates),
                    "teacher_model_path": "/home/admin/model/qwen3.5-27b",
                    "teacher_call_status": "not_run",
                }
            )
            if len(prompts) >= prompt_sample_size:
                return prompts
    return prompts


def build_5way_prompt(span_value: str, context: str, candidates: list[str]) -> str:
    candidate_lines = "\n".join(f"- {candidate}" for candidate in candidates)
    return (
        "You will assess the likelihood that a given text span belongs to each candidate\n"
        "PII type, given the context.\n\n"
        f'Span: "{span_value}"\n'
        f'Context: "{context}"\n\n'
        "This span passes format checks for these candidate types:\n"
        f"{candidate_lines}\n\n"
        "For each candidate, assign EXACTLY ONE of these 5 verdicts:\n"
        '- "strong_for"      : Context strongly suggests this type\n'
        '- "weak_for"        : Context mildly suggests this type\n'
        '- "neutral"         : No evidence either way\n'
        '- "weak_against"    : Context mildly contradicts this type\n'
        '- "strong_against"  : Context strongly contradicts this type\n\n'
        "Also assess NON_PII (the span being not a PII at all).\n\n"
        "Context-specific rules:\n"
        "- bare_span: Do not use strong_for unless the format uniquely identifies one label.\n"
        "- bare_span: Date-like bare spans must remain ambiguous across date-related labels and NON_PII.\n"
        "- weak_context: Avoid strong_for unless the field name explicitly identifies the label.\n"
        "- strong_positive_context: strong_for is allowed when the context clearly identifies the label.\n"
        "- reverse_negative_context: NON_PII should usually be strong_for.\n"
        "- reverse_negative_context: PII labels that only match by format should usually be weak_against or strong_against.\n\n"
        "Output JSON only:\n"
        "{\n"
        '  "verdicts": {\n'
        '    "CANDIDATE_LABEL": "neutral",\n'
        '    "NON_PII": "neutral"\n'
        "  }\n"
        "}"
    )


def filter_candidates(candidates: list[str], training_labels: set[str]) -> list[str]:
    filtered = []
    for candidate in candidates:
        if candidate in training_labels and candidate not in filtered:
            filtered.append(candidate)
    if "NON_PII" in training_labels and "NON_PII" not in filtered:
        filtered.append("NON_PII")
    return filtered


def format_compatible_candidates(span_value: str, candidates: list[str], training_labels: set[str]) -> list[str]:
    allowed = FormatFilter.get_candidates(span_value)
    compatible = [candidate for candidate in candidates if candidate in allowed]
    return filter_candidates(compatible, training_labels)


def make_span_record(
    record_id: str,
    text: str,
    value: str,
    label: str,
    metadata: dict[str, Any],
    distribution: dict[str, float],
    format_candidates: list[str],
    training_weight: float,
) -> dict[str, Any]:
    start = text.index(value)
    end = start + len(value)
    return {
        "id": record_id,
        "text": text,
        "metadata": metadata,
        "spans": [
            {
                "start": start,
                "end": end,
                "value": value,
                "type_distribution": distribution,
                "top_type": label,
                "source": metadata["source"],
                "training_weight": training_weight,
                "format_candidates": format_candidates,
                "rule_verified": False,
            }
        ],
    }


def validate_seed_records(records: list[dict[str, Any]], training_labels: set[str]) -> list[str]:
    errors: list[str] = []
    for record in records:
        text = record.get("text", "")
        spans = record.get("spans", [])
        if record.get("metadata", {}).get("subtype") == "doc_level" and spans != []:
            errors.append(f"{record.get('id')}: document-level negative must have empty spans")
        for span in spans:
            if text[span["start"] : span["end"]] != span.get("value"):
                errors.append(f"{record.get('id')}: offset mismatch")
            labels = set(span.get("type_distribution", {})) | {span.get("top_type")} | set(span.get("format_candidates", []))
            outside = sorted(label for label in labels if label not in training_labels)
            if outside:
                errors.append(f"{record.get('id')}: labels outside training space {outside}")
            dist = span.get("type_distribution", {})
            if abs(sum(float(v) for v in dist.values()) - 1.0) > 0.02:
                errors.append(f"{record.get('id')}: distribution does not sum to 1")
            if record.get("metadata", {}).get("subtype") == "candidate_level":
                if "NON_PII" not in dist or float(dist["NON_PII"]) <= 0.5:
                    errors.append(f"{record.get('id')}: candidate-level negative must be NON_PII-dominant")
    return errors


def validate_prompt_records(prompts: list[dict[str, Any]], training_labels: set[str]) -> list[str]:
    errors: list[str] = []
    for prompt in prompts:
        outside = [label for label in prompt.get("candidate_labels", []) if label not in training_labels]
        if outside:
            errors.append(f"{prompt.get('id')}: candidate labels outside training space {outside}")
        for verdict in VERDICTS:
            if verdict not in prompt.get("prompt", ""):
                errors.append(f"{prompt.get('id')}: missing verdict {verdict}")
    return errors


def _read_json(path: Path) -> Any:
    return json.loads(path.read_text(encoding="utf-8"))


def _write_json(path: Path, payload: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def _write_jsonl(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(json.dumps(row, ensure_ascii=False, separators=(",", ":")) for row in rows) + "\n", encoding="utf-8")


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--root", default=".")
    parser.add_argument("--zero-examples-per-label", type=int, default=20)
    parser.add_argument("--prompt-sample-size", type=int, default=20)
    args = parser.parse_args(argv)
    plan = generate_stage2_artifacts(
        args.root,
        zero_examples_per_label=args.zero_examples_per_label,
        prompt_sample_size=args.prompt_sample_size,
    )
    print(json.dumps(plan, ensure_ascii=False, indent=2))
    return 0


