def generate_stage2_artifacts(
    root: Path | str = ".",
    zero_examples_per_label: int = 20,
    prompt_sample_size: int = 20,
    enforce_expected_counts: bool = True,
) -> dict[str, Any]:
    root = Path(root)
    canonical_labels = _read_json(root / "pii_schema" / "canonical_labels_79.json")
    training_labels = _read_json(root / "pii_schema" / "training_label_space_80.json")
    training_label_set = set(training_labels)
    if enforce_expected_counts and len(canonical_labels) != 79:
        raise ValueError(f"canonical label count must be 79, got {len(canonical_labels)}")
    if enforce_expected_counts and (len(training_labels) != 80 or "NON_PII" not in training_label_set):
        raise ValueError("training label space must be 79 canonical labels plus NON_PII")

    seed_records: list[dict[str, Any]] = []
    seed_records.extend(generate_zero_example_records(training_label_set, zero_examples_per_label))
    seed_records.extend(generate_candidate_level_negatives(training_label_set))
    seed_records.extend(generate_document_level_negatives())
    seed_errors = validate_seed_records(seed_records, training_label_set)
    if seed_errors:
        raise ValueError("stage2 seed validation failed: " + "; ".join(seed_errors[:5]))

    prompts = generate_teacher_prompt_samples(training_label_set, prompt_sample_size)
    prompt_errors = validate_prompt_records(prompts, training_label_set)
    if prompt_errors:
        raise ValueError("stage2 prompt validation failed: " + "; ".join(prompt_errors[:5]))

    generated_dir = root / "data" / "generated"
    reports_dir = root / "reports"
    generated_dir.mkdir(parents=True, exist_ok=True)
    reports_dir.mkdir(parents=True, exist_ok=True)

    _write_jsonl(generated_dir / "stage2_seed_examples.jsonl", seed_records)
    _write_jsonl(generated_dir / "stage2_teacher_prompts_sample.jsonl", prompts)

    zero_counts = {
        label: sum(
            1
            for record in seed_records
            for span in record["spans"]
            if record["metadata"].get("source") == "synthetic_zero_example" and span["top_type"] == label
        )
        for label in ZERO_EXAMPLE_LABELS
    }
    zero_plan = {
        "zero_example_labels": ZERO_EXAMPLE_LABELS,
        "examples_per_label": zero_examples_per_label,
        "generated_counts": zero_counts,
        "generation_mode": "local_draft_examples_no_teacher_calls",
    }
    generation_plan = {
        "stage": "2",
        "teacher_model_path": "/home/admin/model/qwen3.5-27b",
        "teacher_calls_executed": 0,
        "teacher_prompt_sample_count": len(prompts),
        "seed_record_count": len(seed_records),
        "zero_example_record_count": sum(zero_counts.values()),
        "candidate_level_negative_count": sum(1 for r in seed_records if r["metadata"].get("subtype") == "candidate_level"),
        "document_level_negative_count": sum(1 for r in seed_records if r["metadata"].get("subtype") == "doc_level"),
        "context_types": CONTEXT_TYPES,
        "verdicts": VERDICTS,
        "next_gate": "inspect 20 teacher prompts before running batch teacher calls",
    }
    _write_json(reports_dir / "zero_example_label_plan.json", zero_plan)
    _write_json(reports_dir / "stage2_generation_plan.json", generation_plan)
    return generation_plan


def generate_zero_example_records(training_labels: set[str], examples_per_label: int) -> list[dict[str, Any]]:
    records: list[dict[str, Any]] = []
    for label in ZERO_EXAMPLE_LABELS:
        if label not in training_labels:
            raise ValueError(f"zero-example label not in training space: {label}")
        for index in range(examples_per_label):
            text, value = zero_example_text(label, index)
            records.append(
                make_span_record(
                    record_id=f"STAGE2-ZERO-{label}-{index + 1:03d}",
                    text=text,
                    value=value,
                    label=label,
                    metadata={
                        "source": "synthetic_zero_example",
                        "data_category": "stage2_zero_example_positive",
                        "target_label": label,
                    },
                    distribution={label: 0.95, "NON_PII": 0.05},
                    format_candidates=[label, "NON_PII"],
                    training_weight=0.6,
                )
            )
    return records


