def generate_candidate_level_negatives(training_labels: set[str]) -> list[dict[str, Any]]:
    records: list[dict[str, Any]] = []
    for index, group in enumerate(AMBIGUITY_GROUPS, 1):
        candidates = filter_candidates(group["candidates"], training_labels)
        if len(candidates) <= 1:
            continue
        context = group["contexts"]["reverse_negative_context"]
        span_value = group["span"]
        pii_candidates = [candidate for candidate in candidates if candidate != "NON_PII"]
        dist = {"NON_PII": 0.75}
        share = round(0.25 / min(3, len(pii_candidates)), 6) if pii_candidates else 0.0
        for candidate in pii_candidates[:3]:
            dist[candidate] = share
        total = sum(dist.values())
        dist["NON_PII"] = round(dist["NON_PII"] + (1.0 - total), 6)
        records.append(
            make_span_record(
                record_id=f"STAGE2-CAND-NEG-{index:03d}",
                text=context,
                value=span_value,
                label="NON_PII",
                metadata={
                    "source": "synthetic_ambiguous_negative",
                    "data_category": "stage2_candidate_level_negative",
                    "subtype": "candidate_level",
                    "ambiguity_group": group["name"],
                },
                distribution=dist,
                format_candidates=candidates,
                training_weight=0.5,
            )
        )
    return records


